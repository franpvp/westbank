    public void onLoad(){
        
        String nombre = cliente.getNombre();
        String apPaterno = cliente.getApPaterno();
        String apMaterno = cliente.getApMaterno();
        int numCuenta = cliente.getCuenta().getNumCuenta();
        Cuenta cuenta = new Cuenta().obtener(numCuenta);
        int saldoInt = cuenta.getSaldo();
        String saldoStr = String.valueOf(saldoInt);
        lblNombreCliente.setText(nombre + " " + apPaterno + " " + apMaterno);
        lblSaldo.setText("$ " + saldoStr);
        lblNombreCliente.setVisible(true);
        lblSaldo.setVisible(true);
        
    }
